    private val rcon = intArrayOf(0x01, 0x02, 0x04, 0x08, 0x10, 0x20, 0x40, 0x80, 0x1B, 0x36)

    fun decryptEcbNoPadding(key: ByteArray, cipherBytes: ByteArray): ByteArray? {
        if ((key.size != 16 && key.size != 32) || cipherBytes.size % 16 != 0) return null
        val roundKeys = expandKey(key)
        val rounds = key.size / 4 + 6
        val output = ByteArray(cipherBytes.size)
        for (offset in cipherBytes.indices step 16) {
            decryptBlock(cipherBytes, offset, output, roundKeys, rounds)
        }
        return output
    }

    private fun decryptBlock(input: ByteArray, offset: Int, output: ByteArray, roundKeys: ByteArray, rounds: Int) {
        val state = ByteArray(16) { input[offset + it] }
        addRoundKey(state, roundKeys, rounds)
        for (round in rounds - 1 downTo 1) {
            invShiftRows(state)
            invSubBytes(state)
            addRoundKey(state, roundKeys, round)
            invMixColumns(state)
        }
        invShiftRows(state)
        invSubBytes(state)
        addRoundKey(state, roundKeys, 0)
        state.copyInto(output, offset)
    }

    private fun expandKey(key: ByteArray): ByteArray {
        val nk = key.size / 4
        val rounds = nk + 6
        val words = 4 * (rounds + 1)
        val expanded = ByteArray(words * 4)
        key.copyInto(expanded)
        val temp = ByteArray(4)
        var word = nk
        while (word < words) {
            for (i in 0 until 4) temp[i] = expanded[(word - 1) * 4 + i]
            if (word % nk == 0) {
                val first = temp[0]
                temp[0] = (sBox[temp[1].toInt() and 0xFF] xor rcon[word / nk - 1]).toByte()
                temp[1] = sBox[temp[2].toInt() and 0xFF].toByte()
                temp[2] = sBox[temp[3].toInt() and 0xFF].toByte()
                temp[3] = sBox[first.toInt() and 0xFF].toByte()
            } else if (nk > 6 && word % nk == 4) {
                for (i in 0 until 4) temp[i] = sBox[temp[i].toInt() and 0xFF].toByte()
            }
            for (i in 0 until 4) {
                expanded[word * 4 + i] = ((expanded[(word - nk) * 4 + i].toInt() and 0xFF) xor (temp[i].toInt() and 0xFF)).toByte()
            }
            word++
        }
        return expanded
    }

    private fun addRoundKey(state: ByteArray, roundKeys: ByteArray, round: Int) {
        val offset = round * 16
        for (i in 0 until 16) state[i] = ((state[i].toInt() and 0xFF) xor (roundKeys[offset + i].toInt() and 0xFF)).toByte()
    }

    private fun invSubBytes(state: ByteArray) {
        for (i in 0 until 16) state[i] = invSBox[state[i].toInt() and 0xFF].toByte()
    }

    private fun invShiftRows(state: ByteArray) {
        val copy = state.copyOf()
        state[1] = copy[13]; state[5] = copy[1]; state[9] = copy[5]; state[13] = copy[9]
        state[2] = copy[10]; state[6] = copy[14]; state[10] = copy[2]; state[14] = copy[6]
        state[3] = copy[7]; state[7] = copy[11]; state[11] = copy[15]; state[15] = copy[3]
    }

    private fun invMixColumns(state: ByteArray) {
        for (column in 0 until 4) {
            val index = column * 4
            val a0 = state[index].toInt() and 0xFF
            val a1 = state[index + 1].toInt() and 0xFF
            val a2 = state[index + 2].toInt() and 0xFF
            val a3 = state[index + 3].toInt() and 0xFF
            state[index] = (mul(a0, 14) xor mul(a1, 11) xor mul(a2, 13) xor mul(a3, 9)).toByte()
            state[index + 1] = (mul(a0, 9) xor mul(a1, 14) xor mul(a2, 11) xor mul(a3, 13)).toByte()
            state[index + 2] = (mul(a0, 13) xor mul(a1, 9) xor mul(a2, 14) xor mul(a3, 11)).toByte()
            state[index + 3] = (mul(a0, 11) xor mul(a1, 13) xor mul(a2, 9) xor mul(a3, 14)).toByte()
        }
    }

    private fun mul(value: Int, factor: Int): Int {
        var a = value
        var b = factor
        var result = 0
        while (b > 0) {
            if (b and 1 != 0) result = result xor a
            a = if (a and 0x80 != 0) ((a shl 1) xor 0x1B) and 0xFF else (a shl 1) and 0xFF
            b = b shr 1
        }
        return result
    }
